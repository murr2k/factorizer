# Verify test cases for 128-bit factorization

test_cases = [
    (90595490423, 428759, 211297),
    (324625056641, 408337, 794993),
    (2626476057461, 1321171, 1987991),
    (3675257317722541, 91709393, 40075037),
    (7362094681552249594844569, 3011920603541, 2444318974709),
    (6686055831797977225042686908281, 2709353969339243, 2467767559153067),
    (1713405256705515214666051277723996933341, 62091822164219516723, 27594700831518952367),
    (883599419403825083339397145228886129352347501, 79928471789373227718301, 11054876937122958269201),
]

print("Verifying test cases...")
for num, f1, f2 in test_cases:
    product = f1 * f2
    if product == num:
        print(f"✓ {num} = {f1} × {f2}")
    else:
        print(f"✗ {num} ≠ {f1} × {f2} = {product}")
        print(f"  Difference: {num - product}")

